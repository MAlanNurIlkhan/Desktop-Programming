    private void btnimportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnimportActionPerformed
        String filePath = "D:\\Kuliah\\Semester 4\\Desktop Programming\\tugasbesar.txt";
        File file = new File(filePath);

        try{
            FileReader fr = new FileReader(file);
            BufferedReader br = new BufferedReader(fr);
            DefaultTableModel model = (DefaultTableModel) tbldata.getModel();
            Object[] lines = br.lines().toArray();

            for(int i = 0; i < lines.length; i++){
                String[] row = lines[i].toString().split(" ");
                model.addRow(row);
            }
            JOptionPane.showMessageDialog(null, "Data Berhasil Dimport");
        }catch(FileNotFoundException ex){
            Logger.getLogger(Aplikasi_Undian.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnimportActionPerformed

    private void tbldataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbldataMouseClicked
        DefaultTableModel model = (DefaultTableModel) tbldata.getModel();
        txtid.setText(model.getValueAt(tbldata.getSelectedRow(), 0).toString());
        txtnama.setText(model.getValueAt(tbldata.getSelectedRow(), 1).toString());
    }//GEN-LAST:event_tbldataMouseClicked

