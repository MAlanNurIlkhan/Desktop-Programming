    private void btntambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntambahActionPerformed
        DefaultTableModel model = (DefaultTableModel) tbldata.getModel();
        if (txtid.getText().trim().equals("")||txtnama.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Data Belum Lengkap\nTolong Lengkapi");
        }else{
            model.addRow(new Object[]{txtid.getText(), txtnama.getText()});
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
        }
        clear();
    }//GEN-LAST:event_btntambahActionPerformed

    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        DefaultTableModel model = (DefaultTableModel) tbldata.getModel();
        if(tbldata.getSelectedRow()== -1){
            if (tbldata.getSelectedRowCount()== 0){
                JOptionPane.showMessageDialog(null, "Tidak Ada Data Di Tabel");
            }else{
                JOptionPane.showMessageDialog(null, "Silahkan Pilih Data Yang Akan Di Update");
            }
        }else{
            model.setValueAt(txtid.getText(),tbldata.getSelectedRow(), 0);
            model.setValueAt(txtnama.getText(),tbldata.getSelectedRow(), 1);
            JOptionPane.showMessageDialog(null, "Data Berhasil Diupdate");
        }
        clear();
    }//GEN-LAST:event_btnupdateActionPerformed

    private void txtdeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtdeleteActionPerformed
        DefaultTableModel model = (DefaultTableModel) tbldata.getModel();
        if(tbldata.getSelectedRow()== -1){
            if(tbldata.getSelectedRow()== 0){
                JOptionPane.showMessageDialog(null, "Tidak Ada Dalam Tabel");
            }else{
                JOptionPane.showMessageDialog(null, "Silahkan Pilih Data Yang akan Dihapus");
            }
        }else{
            model.removeRow(tbldata.getSelectedRow());
            JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");
        }
        clear();
    }//GEN-LAST:event_txtdeleteActionPerformed

